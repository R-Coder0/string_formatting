#How do you check if a string contains only alphabetic characters?

#Python String isalpha() method is used to check whether all characters in the String are an alphabet.
var1 = "MynameisRishabhPratap"
string1 = var1.isalpha()
print(string1)

#using whitespace
var2 = "My name is Rishabh Pratap"
string2 = var2.isalpha()
print(string2)